def add(x,y):
    print('Given numbers are {} and {} and their addition is {}'.format(x,y,x+y))
def sub(x,y):
    print('Given numbers are {} and {} and their difference is {}'.format(x,y,x-y))
def mul(x,y):
    print('Given numbers are {} and {} and their product is {}'.format(x,y,x*y))
def div(x,y):
    print('Given numbers are {} and {} and their quotient is {}'.format(x,y,x/y))
